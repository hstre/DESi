"""v15.0 — DAX-retrospective financial-statement
fixture (multi-firm, multi-sector, >=10 years).

CRITICAL HONESTY / SAFETY NOTES
===============================
1. Every euro figure below is ILLUSTRATIVE
   SYNTHETIC. The firms are SECTOR ARCHETYPES,
   not real companies - they carry codenames
   (PAYMENTS_ALPHA, AUTO_BETA, ...) precisely so
   that DESi never names, rates, or defames a
   real DAX issuer. The numbers reproduce only
   the *structural* shape of publicly-documented
   concern (cashflow-vs-profit divergence,
   narrative-vs-numbers gaps, goodwill / segment
   opacity, missing disclosure bridges).

2. The ONLY post-hoc information is a coarse
   ``post_hoc_label`` per firm. It lives ONLY on
   the firm record and is read SOLELY by the
   ex-ante validators in ``governance.py``. No
   structure-scoring function reads it.

3. DESi NEVER concludes "fraud" / "Betrug",
   never rates, never advises buying or selling.
   The closed output vocabulary is strictly
   audit-priority (see ``governance.AuditPriority``).
"""
from __future__ import annotations

from dataclasses import dataclass
from enum import Enum


class Sector(str, Enum):
    PAYMENTS    = "payments"
    AUTOMOTIVE  = "automotive"
    CHEMICALS   = "chemicals"
    UTILITIES   = "utilities"
    PHARMA      = "pharma"
    INDUSTRIALS = "industrials"


SECTORS: tuple[str, ...] = tuple(s.value for s in Sector)


class PostHocLabel(str, Enum):
    """Post-hoc validation labels ONLY. Never an
    input to any structure-scoring function."""
    STATEMENTS_LATER_VOIDED = "statements_later_voided"
    GOVERNANCE_EVENT_LATER  = "governance_event_later"
    SCRUTINIZED_NOT_RULED   = "scrutinized_not_ruled"
    NO_ADVERSE_EVENT        = "no_adverse_event"


POST_HOC_LABELS: tuple[str, ...] = tuple(
    p.value for p in PostHocLabel
)

# Post-hoc labels that denote a LATER adverse
# outcome (the ex-ante recall denominator).
ADVERSE_POST_HOC: frozenset[str] = frozenset({
    PostHocLabel.STATEMENTS_LATER_VOIDED.value,
    PostHocLabel.GOVERNANCE_EVENT_LATER.value,
})


@dataclass(frozen=True)
class FirmYear:
    firm_id: str
    sector: str
    fiscal_year: int
    is_synthetic_illustrative: bool
    reported_revenue_eur_m: float
    reported_net_profit_eur_m: float
    operating_cash_flow_eur_m: float
    receivables_eur_m: float
    goodwill_intangibles_eur_m: float
    undisclosed_segment_share: float
    narrative_optimism: float
    disclosure_bridges_required: int
    disclosure_bridges_provided: int

    def to_dict(self) -> dict[str, object]:
        return {
            "firm_id": self.firm_id,
            "sector": self.sector,
            "fiscal_year": self.fiscal_year,
            "is_synthetic_illustrative":
                self.is_synthetic_illustrative,
            "reported_revenue_eur_m":
                _r(self.reported_revenue_eur_m),
            "reported_net_profit_eur_m":
                _r(self.reported_net_profit_eur_m),
            "operating_cash_flow_eur_m":
                _r(self.operating_cash_flow_eur_m),
            "receivables_eur_m":
                _r(self.receivables_eur_m),
            "goodwill_intangibles_eur_m":
                _r(self.goodwill_intangibles_eur_m),
            "undisclosed_segment_share":
                _r(self.undisclosed_segment_share),
            "narrative_optimism":
                _r(self.narrative_optimism),
            "disclosure_bridges_required":
                self.disclosure_bridges_required,
            "disclosure_bridges_provided":
                self.disclosure_bridges_provided,
        }


@dataclass(frozen=True)
class Firm:
    firm_id: str
    sector: str
    # Post-hoc ONLY - read solely by validators.
    post_hoc_label: str
    years: tuple[FirmYear, ...]

    def to_dict(self) -> dict[str, object]:
        return {
            "firm_id": self.firm_id,
            "sector": self.sector,
            "post_hoc_label": self.post_hoc_label,
            "years": [
                y.to_dict() for y in self.years
            ],
        }


def _r(x: float, n: int = 4) -> float:
    return round(x, n)


def _clip01(x: float) -> float:
    return max(0.0, min(1.0, x))


