def _build_firm(p: _Profile) -> Firm:
    rows: list[FirmYear] = []
    for t in range(_N_YEARS):
        year = _START_YEAR + t
        revenue = p.base_revenue * (
            (1.0 + p.revenue_growth) ** t
        )
        profit = revenue * p.net_margin
        ocf_ratio = p.ocf_ratio_base + (
            p.ocf_ratio_drift * t
        )
        ocf = profit * max(0.0, ocf_ratio)
        receivables = revenue * p.receivables_ratio
        goodwill = revenue * p.goodwill_ratio
        narrative = _clip01(
            p.narrative_base + p.narrative_drift * t
        )
        rows.append(FirmYear(
            firm_id=p.firm_id,
            sector=p.sector.value,
            fiscal_year=year,
            is_synthetic_illustrative=True,
            reported_revenue_eur_m=revenue,
            reported_net_profit_eur_m=profit,
            operating_cash_flow_eur_m=ocf,
            receivables_eur_m=receivables,
            goodwill_intangibles_eur_m=goodwill,
            undisclosed_segment_share=(
                p.undisclosed_segment_share
            ),
            narrative_optimism=narrative,
            disclosure_bridges_required=(
                p.bridges_required
            ),
            disclosure_bridges_provided=(
                p.bridges_provided
            ),
        ))
    return Firm(
        firm_id=p.firm_id,
        sector=p.sector.value,
        post_hoc_label=p.post_hoc_label.value,
        years=tuple(rows),
    )


_FIRMS: tuple[Firm, ...] = tuple(
    _build_firm(p) for p in _PROFILES
)


def firms() -> tuple[Firm, ...]:
    return _FIRMS


def firm_ids() -> tuple[str, ...]:
    return tuple(f.firm_id for f in _FIRMS)


def by_id(firm_id: str) -> Firm:
    for f in _FIRMS:
        if f.firm_id == firm_id:
            return f
    raise KeyError(firm_id)


def sectors() -> tuple[str, ...]:
    seen: list[str] = []
    for f in _FIRMS:
        if f.sector not in seen:
            seen.append(f.sector)
    return tuple(seen)


def years() -> tuple[int, ...]:
    return tuple(
        _START_YEAR + t for t in range(_N_YEARS)
    )


